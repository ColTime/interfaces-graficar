    public void generarQR(String nOrden) {
        try {
            QRCode c = new QRCode();
            c.setDataMode(QRCode.MODE_BYTE);
            c.setUOM(udm);
            c.setLeftMargin(mi);
            c.setRightMargin(md);
            c.setTopMargin(ms);
            c.setBottomMargin(min);
            c.setResolution(resol);
            c.setRotate(rot);
            c.setModuleSize(tam);
            
            String archivo= System.getProperty("C:\\Users\\comunicaciones03\\Desktop\\repositoriode vistas\\interfaces-graficar\\Proyecto col_time\\Proyecto\\ColTime\\src\\QR");
            c.renderBarcode();
            
            Desktop ds= Desktop.getDesktop();
            ds.open(new File(archivo));
            
        } catch (Exception e) {
            System.out.print("Error"+e+"\n");
        }
    }

